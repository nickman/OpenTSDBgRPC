package net.opentsdb.tracing;

import io.grpc.Metadata;
import io.grpc.ServerCall;
import io.grpc.ServerCall.Listener;
import io.grpc.ServerCallHandler;
import io.grpc.ServerInterceptor;

/**
 * <p>Title: NoopServerInterceptor</p>
 * <p>Description: </p> 
 * @author Whitehead (nwhitehead AT heliosdev DOT org)
 * <p><code>net.opentsdb.tracing.NoopServerInterceptor</code></p>
 */

public class NoopServerInterceptor implements ServerInterceptor {
	public static final ServerInterceptor INSTANCE = new NoopServerInterceptor();
	
	private NoopServerInterceptor() {}

	/**
	 * {@inheritDoc}
	 * @see io.grpc.ServerInterceptor#interceptCall(io.grpc.ServerCall, io.grpc.Metadata, io.grpc.ServerCallHandler)
	 */
	@Override
	public <ReqT, RespT> Listener<ReqT> interceptCall(ServerCall<ReqT, RespT> call, Metadata headers,
			ServerCallHandler<ReqT, RespT> next) {
		return next.startCall(call, headers);
	}

}
